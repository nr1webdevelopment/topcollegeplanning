export function nameToSlug(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim()
}

export function getAlumnusByName(name: string): NotableAlumnus | undefined {
  const slug = nameToSlug(name)
  return notableAlumni.find(a => a.slug === slug)
}
